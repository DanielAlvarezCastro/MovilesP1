package com.engine;

/**
 * Interfaz imagen
 */
public interface Image{

    /**
     * Devuelve el ancho de la imagen.
     *
     * @return Ancho de la imagen.
     */

    public int getWidth();
    /**
     * Devuelve el alto de la imagen.
     *
     * @return Alto de la imagen.
     */
    public int getHeight();

}